def SortDict(dict):

    # Variables to store the dictionary and returned list
    tempDict = dict
    tempList = []
    # Iterative loop that adds each value to the list
    for x in tempDict:
        tempList.append(tempDict[x])

    # Sorts the list and assigns it to tempList
    tempList = sorted(tempList)

    return tempList

